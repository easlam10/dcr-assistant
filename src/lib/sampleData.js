// Search function
export function searchUserData(query) {
  const searchTerm = query.toLowerCase();
  return userData.filter(
    (user) =>
      user.username.toLowerCase().includes(searchTerm) ||
      user.first_name.toLowerCase().includes(searchTerm) ||
      user.last_name.toLowerCase().includes(searchTerm) ||
      user.gender.toLowerCase().includes(searchTerm)
  );
}

// Get statistics
export function getDataStats() {
  const totalUsers = userData.length;
  const maleUsers = userData.filter((user) => user.gender === "Male").length;
  const femaleUsers = userData.filter(
    (user) => user.gender === "Female"
  ).length;

  const firstNameCounts = {};
  userData.forEach((user) => {
    firstNameCounts[user.first_name] =
      (firstNameCounts[user.first_name] || 0) + 1;
  });

  const mostCommonFirstNames = Object.entries(firstNameCounts)
    .sort(([, a], [, b]) => b - a)
    .slice(0, 5)
    .map(([name, count]) => ({ name, count }));

  return {
    totalUsers,
    maleUsers,
    femaleUsers,
    mostCommonFirstNames,
  };
}
